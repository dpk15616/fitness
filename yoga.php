	<h1 align="center" style="margin-top:30px;"><u>8 TYPES OF YOGA FOR BEGINNERS </u></h1>
	<h3 id= "hd" style="padding-top:50px;"> 1. Hatha Yoga</h3>
	<p id="marg">It’s all about the basics in these slower moving classes that require you to hold each pose for a few breaths.
	In many studios, hatha classes are considered a gentler form of yoga. However, the Sanskrit term “hatha” actually refers to any yoga 
	that teaches physical postures. “It’s a practice of the body, a physical practice that balances these two energies. So, in reality, 
	it is all hatha yoga,” Vilella says.<br>
	<b>Best for:</b> Beginners. Because of its slower pace, hatha is a great class if you’re just starting your yoga practice.
	</p>
	<center><img src="images/hatha.jpg"></center>
	
	<h3 id= "hd" style="padding-top:50px;"> 2. Vinyasa Yoga</h3>
	<p id="marg">Get your flow on in this dynamic practice that links movement and breath together in a dance-like way. In most classes, you won’t
	linger long in each pose and the pace can be quick, so be prepared for your heart rate to rise. Teachers will often pump music, matching the 
	beats to the sequences of the poses.<br>
	<b>Best for:</b> HIIT lovers. Intense exercisers might enjoy Vinyasa because of its faster pace. Runners and endurance athletes are also
	drawn to Vinyasa class because of the continuous movement.
	</p>
	<center><img src="images/vinaysa.jpg"></center>

	<h3 id= "hd" style="padding-top:50px;">3. Iyengar Yoga </h3>
	<p id="marg">Here you’ll get nit-picky about precision and detail, as well as your body’s alignment in each pose. Props, from yoga blocks and
	blankets to straps or a ropes wall, will become your new best friend, helping you to work within a range of motion that is safe and effective.
	Unlike in Vinyasa, each posture is held for a period of time. If you’re new to Iyengar, even if you’ve practiced other types of yoga, it’s good
	to start with a level one class to familiarize yourself with the technique.<br>
	<b>Best for:</b> Detail-oriented yogis. If you like to geek out about anatomy, movement and form, you’ll love Iyengar — teachers 
	share a wealth of information during class. Iyengar can also be practiced at any age and is great for those with injuries 
	(though you should consult with a doctor first), Vilella notes.
	</p>
	<center><img src="images/23.jpg"></center>

	<h3 id= "hd"  style="padding-top:50px;">4. Ashtanga Yoga </h3>
	<p id="marg">If you’re looking for a challenging yet orderly approach to yoga, try Ashtanga. Consisting of six series of specifically 
	sequenced yoga poses, you’ll flow and breathe through each pose to build internal heat. The catch is that you’ll perform the same poses 
	in the exact same order in each class. Some studios will have a teacher calling out the poses, while Mysore style classes 
	(a subset of Ashtanga) require you to perform the series on your own. (But don’t worry — there will always be a teacher in the 
	room to offer assistance if you need it.)<br>
	<b>Best for:</b> Type-A folks. If you’re a perfectionist, you’ll like Ashtanga’s routine and strict guidelines.
	</p>
	<center><img src="images/24.jpg"></center>

	<h3 id= "hd"  style="padding-top:50px;"> 5. Bikram Yoga</h3>
	<p id="marg">Prepare to sweat: Bikram consists of a specific series of 26 poses and two breathing exercises practiced in a room heated 
	to approximately 105 degrees and 40 percent humidity. All Bikram studios practice the same 90-minute sequence so you’ll know exactly 
	what to do once you unroll your mat. Remember, the vigorous practice combined with the heat can make the class feel strenuous. If you’re 
	new to Bikram, take it easy: Rest when you need to and be sure to hydrate beforehand.<br>

	<b>Best for:</b> People who gravitate toward a set routine. Those who are newer to yoga might like Bikram because of its predictable sequence.
	</p>
	<center><img src="images/25.jpg"></center>

	<h3 id= "hd"  style="padding-top:50px;"> 6. Hot Yoga</h3>
	<p id="marg">Hot yoga is similar to Bikram in that it’s practiced in a heated room. But teachers aren’t constrained by the 26-pose Bikram 
	sequence. While the heat will make you feel like you can move deeper into some poses compared to a non-heated class, it can be easy to
	overstretch, so don’t push beyond your capacity.<br>
	<b>Best for:</b> Hardcore sweat lovers. If you love a tough workout that will leave you drenched, sign up for a beginner-friendly heated class.
	</p>
	<center><img src="images/26.jpg"></center>

	<h3 id= "hd"  style="padding-top:50px;">7. Kundalini Yoga </h3>
	<p id="marg">Celebrity devotees including actor Russell Brand and author Gabrielle Bernstein have given Kundalini a cult-like following.
	Yet, this physically and mentally challenging practice looks very different from your typical yoga class. You’ll perform kriyas —
	repetitive physical exercises coupled with intense breath work — while also chanting, singing and meditating. The goal? To break 
	through your internal barriers, releasing the untapped energy residing within you and bringing you a higher level of self-awareness.<br>
	<b>Best for:</b> People looking for a spiritual practice. Those who are seeking something more than a workout may enjoy Kundalini 
	due to its emphasis on the internal aspects of yoga, including breath work, meditation and spiritual energy.
	</p>
	<center><img src="images/27.jpg"></center>

	<h3 id= "hd"  style="padding-top:50px;"> 8. Yin Yoga</h3>
	<p id="marg">If you want to calm and balance your body and mind, this is where you’ll find your zen.
	The opposite of a faster moving practice like Ashtanga, Yin yoga poses are held for several minutes at a time. This meditative practice
	is designed to target your deeper connective tissues and fascia, restoring length and elasticity. You’ll use props so your body can release 
	into the posture instead of actively flexing or engaging the muscles. Like meditation, it may make you feel antsy at first, but stick with 
	it for a few classes and its restorative powers might have you hooked.<br>
	<b>Best for:</b> People who need to stretch and unwind. Keep in mind, Yin yoga is not recommended for people who are super flexible 
	(you might overdo it in some poses) or anyone who has a connective tissue disorder, Vilella says.
	</p>
